package org.scalafx.extras

/**
  * The `modelview` package helps working with FXML.
  * It helps to bind XML-based user interface structure with its logic.
  *
  * There are two cooperating classes `View` for binding FXML to Scala code and `Model` to containing logic for the
  * component.
  * An additional helper `ModelViewComponent` is provided to instantiate the `View` and the corresponding `Model`.
  *
  * The structure of the UI component is defined in a standard JavaFX FXML file.
  * The Scala side of the FXML is in a class `View`.
  * Part of the `View` is automatically generated by ScalaFXML macro, the rest can be customized as needed,
  * including binding of the UI to appropriate parts of the component logic represented by the `Model`.
  * Use of the `ModelViewComponent` is optional and primarily intended to simplify instantiation of the `View`
  * and the `Model`.
  *
  * Below is an example using the classes in `modelview`. The code implements a simple Stop Watch.
  * The complete code is in demos part of the ScalaFX Extras project.
  *
  * First are the FXML definitions representing the structure of the user interface
  * (`org/scalafx/extras/modelview/stopwatch/StopWatchView.fxml`)
  * {{{
  * <?import javafx.geometry.Insets?>
  * <?import javafx.scene.control.*?>
  * <?import javafx.scene.layout.BorderPane?>
  * <?import javafx.scene.layout.HBox?>
  * <?import javafx.scene.text.*?>
  * <BorderPane xmlns="http://javafx.com/javafx/8.0.65" xmlns:fx="http://javafx.com/fxml/1"
  *             fx:controller="org.scalafx.extras.modelview.stopwatch.StopWatchView">
  *     <bottom>
  *         <ButtonBar BorderPane.alignment="CENTER">
  *             <buttons>
  *                 <Button fx:id="startButton" mnemonicParsing="false" text="Start"/>
  *                 <Button fx:id="stopButton" mnemonicParsing="false" text="Stop"/>
  *                 <Button fx:id="resetButton" mnemonicParsing="false" text="Reset"/>
  *             </buttons>
  *         </ButtonBar>
  *     </bottom>
  *     <center>
  *         <HBox>
  *             <children>
  *                 <Label fx:id="minutesLabel" alignment="CENTER" contentDisplay="CENTER" text="99" textAlignment="CENTER"
  *                        BorderPane.alignment="CENTER">
  *                 </Label>
  *                 <Label text=":">
  *                 </Label>
  *                 <Label fx:id="secondsLabel" alignment="CENTER" contentDisplay="CENTER" text="99" textAlignment="CENTER">
  *                 </Label>
  *                 <Label text=".">
  *                 </Label>
  *                 <Label fx:id="fractionLabel" alignment="CENTER" contentDisplay="CENTER" text="99"
  *                        textAlignment="CENTER">
  *                 </Label>
  *             </children>
  *         </HBox>
  *     </center>
  * </BorderPane>
  * }}}
  *
  * The `View` creates connection of the FXML to Scala code and underlying `Model` for the application logic.
  * Note the `@sfxml` annotation and the constructor arguments corresponding to controls defined in FXML,
  * like `minutesLabel`. The last argument to the constructor is the `Model`.
  * (`org.scalafx.extras.modelview.stopwatch.StopWatchView`)
  * {{{
  * package org.scalafx.extras.modelview.stopwatch
  *
  * import org.scalafx.extras.modelview.View
  *
  * import scalafx.Includes._
  * import scalafx.scene.control.{Button, Label}
  * import scalafxml.core.macros.sfxml
  *
  * @sfxml
  * class StopWatchView(minutesLabel: Label,
  *                     secondsLabel: Label,
  *                     fractionLabel: Label,
  *                     startButton: Button,
  *                     stopButton: Button,
  *                     resetButton: Button,
  *                     model: StopWatchModel) extends View {
  *
  *   minutesLabel.text.value = format2d(model.minutes.longValue)
  *   model.minutes.onChange { (_, _, newValue) =>
  *     minutesLabel.text.value = format2d(newValue.longValue)
  *   }
  *   secondsLabel.text.value = format2d(model.seconds.longValue())
  *   model.seconds.onChange { (_, _, newValue) =>
  *     secondsLabel.text.value = format2d(newValue.longValue())
  *   }
  *   fractionLabel.text.value = format2d(model.secondFraction.longValue() / 10)
  *   model.secondFraction.onChange { (_, _, newValue) =>
  *     fractionLabel.text.value = format2d(newValue.longValue() / 10)
  *   }
  *
  *   startButton.disable <== model.running
  *   stopButton.disable <== !model.running
  *   resetButton.disable <== model.running
  *
  *   startButton.onAction = handle {
  *     model.onStart()
  *   }
  *   stopButton.onAction = handle {
  *     model.onStop()
  *   }
  *   resetButton.onAction = handle {
  *     model.onReset()
  *   }
  *
  *   private def format2d(t: Number) = f"${t.longValue()}%02d"
  * }
  * }}}
  *
  * The `Model` implements the logic for the operation of the Stop Watch.
  * Notice that there are no direct references to UI controls.
  * The connection to the UI is through the properties (line `minutes`).
  * The `Model` is not aware how the `View` is implemented.
  * (`org.scalafx.extras.modelview.stopwatch.StopWatchModel`)
  *
  * {{{
  * package org.scalafx.extras.modelview.stopwatch
  *
  * import javafx.{concurrent => jfxc}
  *
  * import org.scalafx.extras._
  * import org.scalafx.extras.modelview.Model
  *
  * import scalafx.Includes._
  * import scalafx.beans.property.{LongProperty, ReadOnlyBooleanProperty, ReadOnlyBooleanWrapper}
  *
  * class StopWatchModel extends Model {
  *
  *   private val _running = ReadOnlyBooleanWrapper(false)
  *
  *   val running: ReadOnlyBooleanProperty = _running.readOnlyProperty
  *
  *   private val counterService = new CounterService()
  *   counterService.period = 10.ms
  *
  *   val minutes = new LongProperty()
  *   val seconds = new LongProperty()
  *   val secondFraction = new LongProperty()
  *
  *   counterService.elapsedTime.onChange { (_, _, newValue) =>
  *     val t = newValue.longValue()
  *     secondFraction.value = t % 1000
  *     seconds.value = (t / 1000) % 60
  *     minutes.value = t / 1000 / 60
  *   }
  *
  *   def onStart(): Unit = {
  *     counterService.doResume()
  *     _running.value = true
  *   }
  *
  *   def onStop(): Unit = {
  *     counterService.doPause()
  *     _running.value = false
  *   }
  *
  *   def onReset(): Unit = counterService.doReset()
  *
  *   private class CounterService extends jfxc.ScheduledService[Long] {
  *
  *     private var timeAccumulator: Long = 0
  *     private var restartTime: Long = 0
  *
  *     val elapsedTime = new LongProperty()
  *
  *     override def createTask(): jfxc.Task[Long] = {
  *       new jfxc.Task[Long]() {
  *         override protected def call(): Long = {
  *           val ct = System.currentTimeMillis()
  *           val et = timeAccumulator + (ct - restartTime)
  *           onFX {elapsedTime.value = et}
  *           et
  *         }
  *       }
  *     }
  *
  *     def doPause(): Unit = {
  *       val ct = System.currentTimeMillis()
  *       timeAccumulator += (ct - restartTime)
  *       onFX {elapsedTime.value = timeAccumulator}
  *       this.cancel()
  *     }
  *
  *     def doResume(): Unit = {
  *       restartTime = System.currentTimeMillis()
  *       this.restart()
  *     }
  *
  *     def doReset(): Unit = {
  *       timeAccumulator = 0
  *       onFX {elapsedTime.value = 0}
  *     }
  *   }
  * }
  * }}}
  *
  * The `ModelViewComponent` implementation is very simple,
  * it only needs instance of the model and information about location of the FXML resource.
  * (`org.scalafx.extras.modelview.stopwatch.StopWatchComponent`)
  * {{{
  * package org.scalafx.extras.modelview.stopwatch
  *
  * import org.scalafx.extras.modelview.ModelViewComponent
  *
  * class StopWatchComponent(val model: StopWatchModel = new StopWatchModel())
  *   extends ModelViewComponent("/org/scalafx/extras/modelview/stopwatch/StopWatchView.fxml")
  * }}}
  *
  * The `ModelViewComponent` can be easily used in an Application class to create UI.
  * (`org.scalafx.extras.modelview.stopwatch.StopWatchApp`)
  * {{{
  * package org.scalafx.extras.modelview.stopwatch
  *
  * import scala.language.implicitConversions
  * import scalafx.application.JFXApp
  * import scalafx.application.JFXApp.PrimaryStage
  * import scalafx.scene.Scene
  * import scalafx.scene.layout.BorderPane
  *
  * object StopWatchApp extends JFXApp {
  *   stage = new PrimaryStage {
  *     scene = new Scene {
  *       title = "StopWatch"
  *       root = new BorderPane {
  *         center = new StopWatchComponent().view
  *       }
  *     }
  *   }
  * }
  * }}}
  */
